//! Token definitions
//! Elegua source code can be tokenized is a sequence of these tokens.
use num_bigint::BigInt;

#[derive(Debug, PartialEq)]
pub enum Tok {
    Ident { ident: String },
    Int { value: BigInt },
    Float { value: f64 },
    String { value: String },

    // Operator (in order of priority):
    Lbracket,
    Rbracket,
    Not,
    Star,
    Slash,
    Percent,
    Plus,
    Minus,
    Greater,
    Less,
    GreaterEqual,
    LessEqual,
    DoubleEqual,
    NotEqual,
    DoubleAmper,
    DoubleVbar,
    Equal,
    PlusEqual,
    MinusEqual,
    StarEqual,
    SlashEqual,

    Lparen,
    Rparen,
    Lbrace,
    Rbrace,
    Colon,
    SemiColon,
    Comma,
    Dot,

    // Keywords (alphabetically):
    And,
    Break,
    Case,
    Class,
    Continue,
    Def,
    Do,
    Elif,
    Else,
    End,
    False,
    For,
    From,
    Global,
    If,
    Import,
    In,
    Module,
    Null,
    Not,
    Or,
    Return,
    Self,
    Super,
    True,
    While,
    Yield,
}

